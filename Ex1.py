# 1.Faça um programa que peça as 4 notas de um aluno e mostre a média final.

# Declaração
n1 = float(input("Digite a nota do aluno: "))
n2 = float(input("Digite a nota do aluno: "))
n3 = float(input("Digite a nota do aluno: "))
n4 = float(input("Digite a nota do aluno: "))

# Operação
M = (n1 + n2 + n3 + n4)/4

# Resultado
print (f"A média final do aluno é, {M:.2f}")